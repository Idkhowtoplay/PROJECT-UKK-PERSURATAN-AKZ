     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $search = $request->input('search');
    
        // If there is a search query, filter the results by 'nama' in the Surat model
        if ($search) {
            $surats = Surat::where('nama', 'like', '%' . $search . '%')->get();
        } else {
            $surats = Surat::all(); // If no search, return all results
        }
    
        $jenisSurats = JenisSurat::all();
        
        return view('depan', compact('jenisSurats', 'surats'));
    }
    


